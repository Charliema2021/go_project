package main

import "fmt"

func main() {
	// 移位运算[都是在用补码运算]
	// 右 >>  低位溢出，符号位不便，并用符号位补溢出的高位
	// 左 <<  符号位不便，低位补0
	// 两个扩展~！~~！~！~！~
	//	1、负数移动位数可能会产生溢出，变成正数
	//	2、当移动到整个字符长度时候，会变成原来的那个数字
	a := 1 >> 2
	b := 1 << 2
	c := -1 >> 2
	// -1的原码是: 1000 0001  -> 反码是：1111 1110
	// -1的补码是：1111 1111
	// >>1011 1111 ->反码是 1011 1110 -> 原码是 1100 0001
	fmt.Printf("a=%b", a)
	fmt.Printf("a的数据类型是%T", a)
	fmt.Printf("b=%b", b)
	fmt.Printf("c=%b", c)
}
